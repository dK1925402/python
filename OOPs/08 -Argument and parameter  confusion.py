''' Parameter: A parameter is a variable in the declaration of a function. 
               In your code, i is a parameter of the function name.
    Argument: An argument is the actual value that is passed to the function when it is called.
              In your code, 'Dhruv' is an argument passed to the function name.'''


def name(i):     # i is a parameter
    print(i)

name('Dhruv')    # 'Dhruv' is an argument
